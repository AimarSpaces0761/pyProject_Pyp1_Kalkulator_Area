# kalkulator area di python
# panjang, lebar dan tinggi

panjang = float(input("Masukkan panjang dari kubus persegi panjang anda: "))
lebar = float(input("Masukkan lebar dari kubus persegi panjang anda: "))
tinggi = float(input("Masukkan tinggi dari kubus persegi panjang anda: "))

vol = panjang * lebar * tinggi

print()
print(f"Volume yang anda masukkan adalah: {vol}cm^2")